# Estrutura de Tuplas
# Tupla é uma estrutura similar lista, porém elementos inseridos em uma tupla não podem ser alterados.
# Em geral são usadas para armazenar estruturas de dados heterogênas. (possui diferentes elementos em um grupo)

tupla = (123, "testando tupla", False, 9.5)

print(tupla.index(9.5))

print(2 in tupla)