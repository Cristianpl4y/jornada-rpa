# Tipos de Estruturas de Dados
# No Python é uma das linguagens que melhor consegue lidar com dados de diferentes tipos e naturezas,
# de forma eficiente e assertiva. Além de possuir um conjunto grande de funcionalidades para manipulação de strings,
# é capaz de trabalhar com várias estruturas de dados, tais como:

# Listas
lista = [1,2,3,4,5,6,7,8,9]

# Sets (Conjuntos): não há repetição de valores e não mantem a ordem
setA = {1,2,3,4,1}
setB = set([1,2,3,9,10])

# Tuplas parecido com as listas , mas as tuplas são indicadas para valores heterogênios (diferença, diversidade, pluralidade ou variedade)
tupla = (1, "Olá", 1.5, False)


# Dicionários (basicamente um json)
dicionario = {
    "lista": lista,
    "sets": [setA, setB],
    "tupla": tupla,
    "coisa_qualquer": "blablabla" 
}
