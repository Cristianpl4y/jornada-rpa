# O símbolo de igual (=) é utilizado para atribuir algum valor a uma variavel. 
# Por exemplo: 

ano = 2023

# Mas quando utilizamos == é como se estivéssemos fazendo uma pergunta: isto é igual a aquilo?

print(ano == 2023) 
# A saida é True (verdadeiro) nossa variavel ano tem o valor 2023!

