#  INTRODUÇÃO
# A biblioteca Pandas é amplamente utilizada para se trabalhar com grandes datasets em análise de dados e tem como base a Linguagem de Programação Python. 
# Apresenta funcionalidades de análise, pré-processamento, exploração e manipulação dse dados. 
# O nome "Pandas" vem de "Panel Data" e "Python Data Analysis" e foi criado por Wes McKinney em 2008.

# Tem uma ótima performance no trabalho com dados usando o objeto DataFrame que se trata de uma estrutura de dados tabular, 
# orientada a colunas, com rótulos (labels) tanto para linhas quanto para colunas e também com arrays unidimensionais chamados
# com o objeto Series.

# Usando o processamento de alto desempenho de arrays da biblioteca NumPy com recursos bastantes flexíveis para manipulação de dados
# tanto de planilhas, quanto em banco de dados relacionais. A biblioteca permite analisar grandes quantidades de dados e 
# gerar conclusões baseadas em teorias estatísticas. Além disso pode realizar um ótimo apoio nas tarefas de pré-processamento dos dados,
# tais como a detecção de valores faltantes, substituição de valores e etc, tornando mais legíveis e relevantes. 
# Além disso, podemos trabalhar com processos estatísticos como contagem, média, mediana, moda, valor máximo, valor mínimo, além de correlações.

# Para a referência completa da biblioteca Pandas acesse a documentação oficial. https://pandas.pydata.org/pandas-docs/stable/index.html

