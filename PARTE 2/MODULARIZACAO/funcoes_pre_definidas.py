# Funções pré-definidas

# Existem centenas de funções que já estão pré-definidas, seja pela própria linguagem ou 
# seja pelo uso de bibliotecas e pacotes adicionais.

# São chamadas de funções nativas, exemplo:

a = 3.14
a = round(a) # Função de arredondamento simples
print(a)


# Podemos encadear funções juntas
import numpy as np
b = np.arange(15).reshape(3, 5)
print(b)